public class TPC02 {

  public static void main(String[] args) {
    // 프로그래밍의 3대 요소 : 변수, 자료형(Data Type), 할당(=)
    // 1+1=2
    int a, b, c;
    a = 1;
    b = 1;
    c = a + b;
    System.out.println(c);

    float f; // float, double
    f = 34.5f;
    System.out.println(f);

    double z;
    z = 3.45;
    System.out.println(z);

    char d;
    d = 'a';
    System.out.println(d);

    boolean g;
    g = true;
    System.out.println(g);

  }
}
